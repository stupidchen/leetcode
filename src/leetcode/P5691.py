from collections import Counter

M = 6

class Solution:
    def minOperations(self, nums1, nums2):
        if len(nums1) > len(nums2):
            nums1, nums2 = nums2, nums1

        n1, n2 = len(nums1), len(nums2)
        if n1 * 6 < n2 * 1:
            return -1

        if sum(nums1) > sum(nums2):
            nums1, nums2 = nums2, nums1
        n1, n2 = len(nums1), len(nums2)
        s1, s2 = sum(nums1), sum(nums2)
        m = s2 - s1
        c1, c2 = Counter(nums1), Counter(nums2)

        r = 0
        for i in reversed(range(1, 6)):
            if 6 - i in c1:
                v = c1[6 - i]
                if m - v * i > M:
                    m -= v * i
                    c1[6 - i] = 0
                    r += v
                else:
                    t = (m - M) // i
                    c1[6 - i] -= t
                    m -= t * i
                    r += t
                    break

            if i + 1 in c2:
                v = c2[i + 1]
                if m - v * i > M:
                    m -= v * i
                    c2[i + 1] = 0
                    r += v
                else:
                    t = (m - M) // i
                    c2[i + 1] -= t
                    m -= t * i
                    r += t
                    break

        f = [-1] * (m + 1)
        f[0] = 0
        for k, v in c1.items():
            for i in reversed(range(1, m + 1)):
                for j in range(1, v + 1):
                    for q in range(1, 7 - k):
                        t = i - j * q
                        if t >= 0:
                            if f[t] != -1 and (f[i] == -1 or f[t] + j < f[i]):
                                f[i] = f[t] + j
        for k, v in c2.items():
            for i in reversed(range(1, m + 1)):
                for j in range(1, v + 1):
                    for q in range(1, k):
                        t = i - j * q
                        if t >= 0:
                            if f[t] != -1 and (f[i] == -1 or f[t] + j < f[i]):
                                f[i] = f[t] + j
        return f[m] + r


